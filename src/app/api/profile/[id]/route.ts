    console.log('üîß API Profile Public: –ù–∞–π–¥–µ–Ω –ø—Ä–æ—Ñ–∏–ª—å:', profile)

    // –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ç–æ–ª—å–∫–æ –ø—É–±–ª–∏—á–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é
